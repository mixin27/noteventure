/// App spacing constants
class AppSpacing {
  AppSpacing._();

  // Base spacing unit
  static const double base = 8.0;

  // Spacing values
  static const double xs = base * 0.5; // 4
  static const double sm = base; // 8
  static const double md = base * 2; // 16
  static const double lg = base * 3; // 24
  static const double xl = base * 4; // 32
  static const double xxl = base * 6; // 48
  static const double xxxl = base * 8; // 64

  // Page padding
  static const double pagePaddingHorizontal = md;
  static const double pagePaddingVertical = md;

  // Card padding
  static const double cardPadding = md;

  // Button padding
  static const double buttonPaddingHorizontal = lg;
  static const double buttonPaddingVertical = sm;

  // Border radius
  static const double radiusXs = 4.0;
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 24.0;
  static const double radiusFull = 9999.0;

  // Icon sizes
  static const double iconXs = 16.0;
  static const double iconSm = 20.0;
  static const double iconMd = 24.0;
  static const double iconLg = 32.0;
  static const double iconXl = 48.0;

  // Avatar sizes
  static const double avatarSm = 32.0;
  static const double avatarMd = 48.0;
  static const double avatarLg = 64.0;
  static const double avatarXl = 96.0;
}
